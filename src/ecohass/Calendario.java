    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try {
            Connection c = conexion();
            ps = c.prepareStatement("SELECT titulo,evento FROM evento WHERE fechacumplimiento=\"" + lafecha(calendario.getDate().toString()) + "\"");
            ResultSet r = ps.executeQuery();
            String rr = "";
            int co = 1;
            String newline = System.lineSeparator();
            while (r.next()) {
                rr = rr + co + "- TITULO DEL EVENTO:" + r.getString("titulo") + newline + r.getString("evento") + newline;
                co++;
            }
            ps = c.prepareStatement("SELECT titulo,tarea FROM tareas WHERE fecha=\"" + lafecha(calendario.getDate().toString()) + "\"");
            r=ps.executeQuery();
            while(r.next()){
                rr=rr+co+"- TITULO DE LA TAREA:"+r.getString("titulo")+newline+ r.getString("tarea")+newline;
                co++;
            }
            if (co == 1) {
                rr = "no se encontraron ni eventos ni tareas programadas para esta fecha.";
            }
            ta.setText(rr);
        } catch (SQLException ex) {
            Logger.getLogger(Calendario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1MouseClicked

