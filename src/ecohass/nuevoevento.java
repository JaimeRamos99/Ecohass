    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (t.getText().equals("") || f.getDate() == null || ta.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "faltÃ³ rellenar alguno de los campos obligatorios", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                Connection c = conexion();
                ps = c.prepareStatement("INSERT INTO evento(titulo,evento,fechacumplimiento,fechadesubida) VALUES(?,?,?,?)");
                ps.setString(1, t.getText());
                ps.setString(2, ta.getText());
                ps.setString(3,  lafecha(f.getDate().toString()));
                ps.setString(4, lafecha(new Date().toString()));
                int r = ps.executeUpdate();
                if (r > 0) {
                    JOptionPane.showMessageDialog(null, "Evento programado exitosamente.");
                    t.setText("");
                    ta.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "El evento no se pudo programar.");
                }
            } catch (SQLException ex) {
                Logger.getLogger(nuevoevento.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

